.MODEL SMALL
.STACK 100H
.DATA

MYBIODATA	DB "NAME: SYED MUHAMMAD MURTAZA",0DH,0AH
	DB "ROLL NUMBER: 17B-044-CE",0DH,0AH
	DB "SECTION: E",0DH,0AH,0DH,0AH,'$'

ENTER_STR DB "ENTER STRING: $"
ENTER_NUMBER DB 0DH,0AH,0DH,0AH,"ENTER NUMBER BY WHICH YOU WANT TO TRANSPOSE THE STRING: $"  
RESULT DB 0DH,0AH,0DH,0AH,"RESULT: $"

ARRAY1 DB ?



.CODE
.STARTUP

MOV AH,09H
MOV DX,OFFSET MYBIODATA
INT 21H

MOV AH,09H
MOV DX, OFFSET ENTER_STR
INT 21H 

MOV SI,OFFSET ARRAY1

MOV CX,0

MOV AX,0

MOV DL,0

LOOP1:
MOV AH,01H
INT 21H
CMP AL,0DH
JE EXIT

MOV [SI],AL
INC SI
INC CX

JMP LOOP1

EXIT:

MOV AH,09H
MOV DX, OFFSET ENTER_NUMBER
INT 21H

MOV AH,01H
INT 21H   ;NUMBER INPUT
SUB AL,30H

MOV BX,0H

MOV DI, OFFSET ARRAY1

MOV AH,0H
MOV SI,AX

MOV AH,09H
MOV DX, OFFSET RESULT
INT 21H

LOOP2:
MOV BX,0H
MOV BX,[DI]
ADD BX,SI

MOV AH,02H
MOV DX,BX
INT 21H 

INC DI
INC BP

CMP CX,BP
JE EXIT2:


JMP LOOP2

EXIT2:

.EXIT
END